let cart = [];

function showCategory(category) {
    const productsContainer = document.getElementById('products');
    productsContainer.innerHTML = '';

    productsData[category].forEach(product => {
        const productCard = document.createElement('div');
        productCard.className = 'product-card';

        const productImage = document.createElement('img');
        productImage.src = product.image;
        productImage.alt = product.name;

        const productInfo = document.createElement('div');
        productInfo.className = 'product-info';

        const productName = document.createElement('h3');
        productName.textContent = product.name;

        const productPrice = document.createElement('p');
        productPrice.textContent = `Price: $${product.price}`;

        const productRating = document.createElement('p');
        productRating.textContent = `Rating: ${product.rating}`;

        const addToCartButton = document.createElement('button');
        addToCartButton.textContent = 'Add to Cart';
        addToCartButton.addEventListener('click', () => openProductModal(product));

        productInfo.appendChild(productName);
        productInfo.appendChild(productPrice);
        productInfo.appendChild(productRating);
        productInfo.appendChild(addToCartButton);

        productCard.appendChild(productImage);
        productCard.appendChild(productInfo);

        productsContainer.appendChild(productCard);
    });

    // Update selected category styling
    const categoryLinks = document.querySelectorAll('.categories a');
    categoryLinks.forEach(link => link.classList.remove('selected'));
    document.querySelector(`.categories a[data-category="${category}"]`).classList.add('selected');
}

function openProductModal(product) {
    const modal = document.getElementById('productModal');
    document.getElementById('modalImage').src = product.image;
    document.getElementById('modalName').textContent = product.name;
    document.getElementById('modalDescription').textContent = product.description;
    document.getElementById('modalPrice').textContent = `Price: $${product.price}`;
    document.getElementById('quantity').value = 1;
    document.getElementById('quantityValue').textContent = 1;

    const addToCartBtn = document.getElementById('addToCartBtn');
    addToCartBtn.onclick = () => addToCart(product);

    modal.style.display = 'block';
}

function closeProductModal() {
    const modal = document.getElementById('productModal');
    modal.style.display = 'none';
}

function addToCart(product) {
    const quantity = document.getElementById('quantity').value;
    cart.push({ ...product, quantity });
    alert(`${product.name} added to cart`);
    updateCartCount();
    closeProductModal();
}

function showCart() {
    const modal = document.getElementById('cartModal');
    const cartItemsContainer = document.getElementById('cartItems');
    cartItemsContainer.innerHTML = ''; // Clear previous items

    let total = 0;

    cart.forEach(item => {
        const cartItem = document.createElement('div');
        cartItem.className = 'cart-item';

        const itemImage = document.createElement('img');
        itemImage.src = item.image;
        itemImage.alt = item.name;

        const itemDetails = document.createElement('div');
        const itemName = document.createElement('p');
        itemName.textContent = `${item.name} - Quantity: ${item.quantity}`;

        const itemPrice = document.createElement('p');
        itemPrice.textContent = `Price: $${item.price}`;

        itemDetails.appendChild(itemName);
        itemDetails.appendChild(itemPrice);
        cartItem.appendChild(itemImage);
        cartItem.appendChild(itemDetails);

        cartItemsContainer.appendChild(cartItem);

        total += item.price * item.quantity;
    });

    document.getElementById('cartTotal').textContent = `$${total.toFixed(2)}`;

    modal.style.display = 'block';
}

function closeCartModal() {
    const modal = document.getElementById('cartModal');
    modal.style.display = 'none';
}

function updateCartCount() {
    const cartCount = document.getElementById('cart-count');
    cartCount.textContent = cart.length;
}

document.addEventListener('DOMContentLoaded', () => {
    showCategory('vodka');

    // Add event listeners to category links
    const categoryLinks = document.querySelectorAll('.category-link');
    categoryLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const category = link.getAttribute('data-category');
            showCategory(category);
        });
    });

    // Add event listener to close modal
    const modalCloseButtons = document.querySelectorAll('.close');
    modalCloseButtons.forEach(button => {
        button.addEventListener('click', closeProductModal);
        button.addEventListener('click', closeCartModal);
    });

    // Update quantity value on input change
    const quantityInput = document.getElementById('quantity');
    quantityInput.addEventListener('input', (e) => {
        document.getElementById('quantityValue').textContent = e.target.value;
    });

    // Add event listener to cart icon
    document.getElementById('cart-icon').addEventListener('click', (e) => {
        e.preventDefault();
        showCart();
    });
});
